package graphics;

public class Ball {

	public float x;
	public float y;

	public float speedX;
	public float speedY;

	public int radius;

	public Ball(float x, float y, float speedX, float speedY, int radius) {
		this.x = x;
		this.y = y;
		this.speedX = speedX;
		this.speedY = speedY;
		this.radius = radius;
	}

}